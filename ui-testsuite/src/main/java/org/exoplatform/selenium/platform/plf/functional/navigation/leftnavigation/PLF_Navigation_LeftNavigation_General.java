	/**
	 * Case ID:76807.
	 * Test Case Name: Display "Left Navigation" for Social Intranet.
	 * Pre-Condition: 
	 * Post-Condition: 
	 * Done with OSs and browsers : 
	 * Generated by chinhdtt at 2014/03/18 15:02:25
	 */
	@Test
	public  void test01_DisplayLeftNavigationForSocialIntranet() {
		info("Test 1: Display Left Navigation for Social Intranet");
		String url = baseUrl+"/acme/";
		/*
		- Connect to Intranet
		 *Input Data: 
		 *Expected Outcome: 
		- The Left Navigation is displayed: Company List" and "My Spaces" panels		*/
		waitForAndGetElement(ELEMENT_LEFT_NAVIGATION_COMPANY);
		waitForAndGetElement(ELEMENT_HOME_PAGE);
		waitForAndGetElement(ELEMENT_CONNECTION_PAGE);
		waitForAndGetElement(ELEMENT_WIKI_PAGE);
		waitForAndGetElement(ELEMENT_PERSONAL_DOCUMENTS);
		waitForAndGetElement(ELEMENT_FORUM_PAGE);
		waitForAndGetElement(ELEMENT_CALENDAR_PAGE);
		waitForAndGetElement(ELEMENT_SPACE_NAVIGATION);

		/*
		- Connect to other sites, eg acme site
		 *Input Data: 
		 *Expected Outcome: 
		- The Left Navigation isn't displayed		*/ 
		info("Connect acme site");
		driver.get(url);
		waitForElementNotPresent(ELEMENT_LEFT_NAVIGATION_COMPANY);
		waitForElementNotPresent(ELEMENT_HOME_PAGE);
		waitForElementNotPresent(ELEMENT_CONNECTION_PAGE);
		waitForElementNotPresent(ELEMENT_WIKI_PAGE);
		waitForElementNotPresent(ELEMENT_PERSONAL_DOCUMENTS);
		waitForElementNotPresent(ELEMENT_FORUM_PAGE);
		waitForElementNotPresent(ELEMENT_CALENDAR_PAGE);
		waitForElementNotPresent(ELEMENT_SPACE_NAVIGATION);
	}
